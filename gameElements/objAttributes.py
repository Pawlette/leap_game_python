class Position:
    def __init__(self):
        x = 0
        y = 0
        z = 0

    def __init__(self, x, y, z):
        self.x = x
        self.y = y
        self.z = z

    def setX(self, val):
        self.x = val
    def setY(self, val):
        self.y = val
    def setZ(self, val):
        self.z = val

